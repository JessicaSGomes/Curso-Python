num1 = 5
num2 = 3

#operadores aritméticos
print(num1 + num2) #soma
print(num1 - num2) #subtração
print(num1 * num2) #multiplicação
print(num1 / num2) #divisão
print(num1 % num2) #módulo
print(num1 ** num2) #exponenciação
print(num1 // num2) #divisão inteira

print((3 + 5)*(3 + 5))

#função abs: retorna o número absoluto (mesmo número, sem levar em conta o seu sinal) 
print(abs(-15))
#função pow: faz a exponenciação
print(pow(3,3)) # 3³
#função max e min: passa vários números e retorna o máximo e o mínimo
print(max(1, 45, -23, 5, 22))
print(min(1, 45, -23, 5, 22))
#função round: que faz arredendamento por aproximação
print(round(8.7)) #arredonda para 9


#outras funções é preciso importar uma biblioteca
import math

print(math.floor(8.7)) #arredonda para baixo
print(math.ceil(8.7)) #arredonda para cima
print(math.sqrt(9)) #raiz quadrada


